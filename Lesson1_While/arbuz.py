# ввод количества арбузов
n = int(input("Введите количество арбузов: "))

# ввод массы каждого арбуза
masses = list(map(int, input("Введите массу каждого арбуза через пробел: ").split()))

# находим самый легкий и самый тяжелый арбуз
min_mass = min(masses)
max_mass = max(masses)

# вывод результата
print(f"Самый легкий арбуз: {min_mass}, самый тяжелый арбуз: {max_mass}")
